package interactivepriorityqueue;

class Client {
    private int id;
    private String membership;

    public Client(int id, String membership) {
        this.id = id;
        this.membership = membership;
    }

    public int getId() {
        return id;
    }

    public String getMembership() {
        return membership;
    }
}